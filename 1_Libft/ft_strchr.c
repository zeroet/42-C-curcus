#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (*s == '\0')
		return (0);
	while (*s)
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
	}
	if ((char)c == '\0')
		return ((char*)s);
	return (0);
}
