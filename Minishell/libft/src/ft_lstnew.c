#include "../include/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (!lst)
		return ((void *)0);
	lst->content = content;
	lst->next = (void *)0;
	return (lst);
}
